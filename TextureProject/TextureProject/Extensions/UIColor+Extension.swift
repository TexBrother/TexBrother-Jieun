import Foundation
import UIKit

extension UIColor {
    @nonobjc class var coralBlue: UIColor {
        return UIColor(red: 58/255, green: 130/255, blue: 247/255, alpha: 1.0)
    }
    
    @nonobjc class var lineGrey: UIColor {
        return UIColor(red: 18/255, green: 18/255, blue: 19/255, alpha: 1.0)
    }
    
    @nonobjc class var buttonGrey: UIColor {
        return UIColor(red: 28/255, green: 28/255, blue: 30/255, alpha: 1.0)
    }
}
